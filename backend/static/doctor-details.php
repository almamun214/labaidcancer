                            <div class="doctor-content">
                                <div class="research-title">Graft-Versus-Host Disease and Graft-Versus-Leukemia Effect
                                </div>

                                <p class="research-info">The biology of acute graft-versus-host disease (GVHD) must be
                                    considered in the context of a host that has been injured by the underlying disease,
                                    conditioning regimen, and infection. These factors result in an environment that
                                    supports and sustains the clinical syndrome of acute GVHD through induction of
                                    inflammatory mediators such as chemokines, adhesion molecules, cytokines, and
                                    cellular effectors. If this cycle can be interrupted, GVHD might be ameliorated.
                                    Thus we are studying novel regimens to control GVHD, through both cellular
                                    engineering and application of new immunosuppressants, including sirolimus and
                                    denileukin diftitox.A second related interest is to understand and harness the
                                    graft-versus-leukemia (GVL) effect. Previous efforts to control GVHD, such as T cell
                                    depletion, have resulted in loss of GVL effect. Inhibition of inflammatory cytokines
                                    may result in reductions in GVHD with sparing of GVL effect. Furthermore, we have
                                    shown that we can use GVL effect to induce remissions in relapsed patients, but at
                                    the risk of inducing GVHD. We are studying whether the limitations of T cell
                                    depletion can be overcome with the judicious use of donor lymphocytes preemptively
                                    administered after transplantation. We are also assessing whether GVL effect can be
                                    harnessed to allow a reduction in transplantation conditioning and its associated
                                    toxicity.</p>


