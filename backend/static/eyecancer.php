                                <h3>Risk factors</h3>
                                <ul>
                                   <li>Age Gender-Eye Melanoma can happened at any age but more common in old age and it is more occurs in male than female</li>
                                    <li>Ethnicity-Its more common in Asian and Hispanic ethnicity.</li>
                                    <li>Eye color-Light color people suffers more than dark color people.</li>
                                    <li>Genetic inheritance- BAP1 cancer syndrome is a rare inherited condition in which family members are at increased risk for uveal eye melanoma.</li>
                                    <li>Moles-Different types of Mole on eyes and skin can cause melanoma in eyes.</li>
                                    <li>Family History- Uveal eye melanomas can run in some families.</li>
                                </ul>
                              
