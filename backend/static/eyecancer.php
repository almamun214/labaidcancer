                                <p>Tumors of the eyes are the secondary spread of cancer from various part of the body like Lungs, Breast, Bowel and Prostate.
The common Tumors which primarily arises from Eyes are-
Basal cell carcinoma- common eyelid tumor is called basal cell carcinoma. This tumor can grow around the eye but rarely spreads to other parts of the body.
Retinoblastoma-Common for Children, Retinoblastoma is a cancer of the retina which is the eye's light-sensitive tissue. It may arise in both eyes.
Melanoma-It arises from uncontrolled growth of Melanocytes. Common for Adults. It happened in 60 or above aged.
Others are-Intraocular Lymphoma, Hemangioma.
</p>
