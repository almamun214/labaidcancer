                                <ul>
                                <li>shadows, flashes of light in your vision.</li>
                                <li>a dark patch in your eye that's becoming bigger.</li>
                                <li>partial or total loss of vision.</li>
                                <li>bulging of one eye or both</li>
                                <li>Blurred vision.</li>
                                <li>pain in or around your eye, although this is rare.</li>
                                <li>A lump/growth on your eyelid or in your eye that's increasing in size.</li>
