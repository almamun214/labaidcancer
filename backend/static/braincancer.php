                                <h3>Diagnosis</h3>
                                Advance Genomic Test: It helps to analysis genomic profile of Brain Tumors.
                                Biopsy: Take out small piece of Cancerous Tissue and evaluation of Tumors and help to plan out for treatment processes.
                                Nuclear Medicine Bone scan: reveal the location of the cancer, as well as whether the brain cancer has spread to the bone
                                Angiography: It takes the images and possible location of Tumors and help to plan for operation of Tumors.
                                Other imaging Test: MRI, CT and PET Scan all are used to locate the types & fast growing of tumors.

                                Brain cancer treatment options include:
                                Surgery
                                Surgery is preferred for the first line of treatment for brain cancer. Procedures may include minimally invasive surgical techniques to safely remove of tumors.
                                Chemotherapy
                                This treatment is frequently used to treat high-grade brain cancer, either alone or in combination with other brain cancer treatments, such as surgery and radiation therapy.
                                Neurosurgery
                                This specialized type of surgery performed on the nervous system, particularly the brain and spinal cord, typically focuses on removing the entire brain tumor and other surrounding structures.
                                Radiation therapy
                                This treatment is used to destroy Cancerous brain tumor cells and to relieve symptoms caused by a tumor.
                                Targeted therapy
                                Targeted therapy drugs are designed to target specific pathways or abnormalities in cells involved in brain tumor growth.

